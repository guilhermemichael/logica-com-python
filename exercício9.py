"""
9.Efetuar a leitura de um valor numérico inteiro e apresentar o resultado do valor lido elevado ao quadrado.
"""
# Solicitar ao usuário um valor numérico inteiro
num = int(input("Digite um valor numérico inteiro: "))

# Calcular o valor lido elevado ao quadrado
quadrado = num ** 2

# Apresentar o resultado
print(f"O valor lido elevado ao quadrado é: {quadrado}")